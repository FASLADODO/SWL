%
% using Paul's notation
%	Fu's book pp. 38
%

%addpath('../../src/robot_kinematics');

dof = 6;

%------------------------------------------------------------------------------
d1 = 150.0;
d2 = 100.0;
d3 = 300.0;
d6 = 50.0;

%------------------------------------------------------------------------------
T0 = eye(4);
T1 = [
	0	0	1	0
	-1	0	0	0
	0	-1	0	d1
	0	0	0	1
];
T2 = [
	0	1	0	d2
	0	0	1	0
	1	0	0	d1
	0	0	0	1
];
T3 = [
	-1	0	0	d2
	0	0	1	d3
	0	1	0	d1
	0	0	0	1
];
T4 = [
	-1	0	0	d2
	0	-1	0	d3
	0	0	1	d1
	0	0	0	1
];
T5 = [
	-1	0	0	d2
	0	0	1	d3
	0	1	0	d1
	0	0	0	1
];
T6 = [
	-1	0	0	d2
	0	0	1	d3 + d6
	0	1	0	d1
	0	0	0	1
];

T = cell(1, dof + 1);
T{1} = T0;
T{2} = T1;
T{3} = T2;
T{4} = T3;
T{5} = T4;
T{6} = T5;
T{7} = T6;

%------------------------------------------------------------------------------
a = zeros(dof, 1);
alpha = zeros(dof, 1);
d = zeros(dof, 1);
theta = zeros(dof, 1);

for idx = 1:dof
	[ a(idx), alpha(idx), d(idx), theta(idx) ] = calc_dh(T{idx}, T{idx+1}, 'craig');
end;
dh_param1 = [ a, alpha, d, theta ];

%------------------------------------------------------------------------------

dh_param2 = calc_dh_param(T, 'paul');
